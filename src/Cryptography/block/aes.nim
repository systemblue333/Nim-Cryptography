template xtime(x: uint8): uint8 =
  ((x shl 1) xor (((x shr 7) and 1) * 0x1B'u8)) and 0xFF'u8

template gfMul(a: uint8, b: static uint8): uint8 =
  when b == 0x01: a
  elif b == 0x02: xtime(a)
  elif b == 0x03: xtime(a) xor a
  elif b == 0x09:
    let a2 = xtime(a)
    let a4 = xtime(a2)
    let a8 = xtime(a4)
    a8 xor a
  elif b == 0x0B:
    let a2 = xtime(a)
    let a4 = xtime(a2)
    let a8 = xtime(a4)
    a8 xor a2 xor a
  elif b == 0x0D:
    let a2 = xtime(a)
    let a4 = xtime(a2)
    let a8 = xtime(a4)
    a8 xor a4 xor a
  elif b == 0x0E:
    let a2 = xtime(a)
    let a4 = xtime(a2)
    let a8 = xtime(a4)
    a8 xor a4 xor a2
  else:
    var res: uint8 = 0
    var aa = a
    var bb = b
    for i in static(0 ..< 8):
      let bMask: uint8 = (0'u8 - (bb and 0x01'u8))
      res = res xor (aa and bMask)

      let hiBitMask = (0'u8 - (aa shr 7))
      aa = (aa shl 1) xor (0x1B'u8 and hiBitMask)

      bb = bb shr 1

    res

